public class App {
    public static void main(String[] args) throws Exception {
        
        int x = 144;
        int y = 999;

        System.out.printf("x = %d%n", x);
        System.out.printf("y = %d%n", y);
        System.out.printf("x + y = %d%n", x + y);
        System.out.printf("x - y = %d%n", x - y);
        System.out.printf("x / y = %.8f%n", (float) x / y);
        System.out.printf("x * y = %d%n", x * y);
    }
}
